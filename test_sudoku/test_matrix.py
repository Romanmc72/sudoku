#! /usr/bin/env python3
import unittest
from textwrap import dedent

from samples.samples import EASY_SAMPLE_MATRIX
from samples.samples import MEDIUM_SAMPLE_MATRIX
from samples.samples import HARD_SAMPLE_MATRIX

from sudoku_objects.matrix import Matrix

TEST_CASE_VERBOSE = False


class test_matrix(unittest.TestCase):
    def testMatrix(self):
        self.maxDiff = None
        self.assertEqual(
            Matrix(EASY_SAMPLE_MATRIX).__str__(),
            dedent("""\
                ++===+===+===++===+===+===++===+===+===++
                || 7 |   |   ||   |   |   ||   |   | 3 ||
                ++---+---+---++---+---+---++---+---+---++
                ||   | 6 | 4 ||   |   |   || 9 | 8 |   ||
                ++---+---+---++---+---+---++---+---+---++
                ||   | 9 |   || 7 |   | 3 ||   | 1 |   ||
                ++===+===+===++===+===+===++===+===+===++
                ||   |   | 9 || 3 |   | 4 || 6 |   |   ||
                ++---+---+---++---+---+---++---+---+---++
                ||   |   |   ||   | 9 |   ||   |   |   ||
                ++---+---+---++---+---+---++---+---+---++
                ||   |   | 7 || 6 |   | 2 || 1 |   |   ||
                ++===+===+===++===+===+===++===+===+===++
                ||   | 8 |   || 4 |   | 7 ||   | 3 |   ||
                ++---+---+---++---+---+---++---+---+---++
                ||   | 1 | 3 ||   |   |   || 7 | 6 |   ||
                ++---+---+---++---+---+---++---+---+---++
                || 6 |   |   ||   |   |   ||   |   | 4 ||
                ++===+===+===++===+===+===++===+===+===++
                """)
        )

    def testSolveEasy(self):
        self.maxDiff = None
        EZ = Matrix(EASY_SAMPLE_MATRIX, verbose=TEST_CASE_VERBOSE)
        EZ.solve()
        self.assertEqual(
            EZ.__str__(),
            dedent("""\
            ++===+===+===++===+===+===++===+===+===++
            || 7 | 5 | 1 || 9 | 8 | 6 || 4 | 2 | 3 ||
            ++---+---+---++---+---+---++---+---+---++
            || 3 | 6 | 4 || 2 | 1 | 5 || 9 | 8 | 7 ||
            ++---+---+---++---+---+---++---+---+---++
            || 2 | 9 | 8 || 7 | 4 | 3 || 5 | 1 | 6 ||
            ++===+===+===++===+===+===++===+===+===++
            || 1 | 2 | 9 || 3 | 7 | 4 || 6 | 5 | 8 ||
            ++---+---+---++---+---+---++---+---+---++
            || 5 | 4 | 6 || 1 | 9 | 8 || 3 | 7 | 2 ||
            ++---+---+---++---+---+---++---+---+---++
            || 8 | 3 | 7 || 6 | 5 | 2 || 1 | 4 | 9 ||
            ++===+===+===++===+===+===++===+===+===++
            || 9 | 8 | 5 || 4 | 6 | 7 || 2 | 3 | 1 ||
            ++---+---+---++---+---+---++---+---+---++
            || 4 | 1 | 3 || 8 | 2 | 9 || 7 | 6 | 5 ||
            ++---+---+---++---+---+---++---+---+---++
            || 6 | 7 | 2 || 5 | 3 | 1 || 8 | 9 | 4 ||
            ++===+===+===++===+===+===++===+===+===++
            """)
        )

    def testSolveMedium(self):
        self.maxDiff = None
        MD = Matrix(MEDIUM_SAMPLE_MATRIX, verbose=TEST_CASE_VERBOSE)
        MD.solve()
        self.assertEqual(
            MD.__str__(),
            dedent("""\
                ++===+===+===++===+===+===++===+===+===++
                || 3 | 8 | 5 || 6 | 4 | 2 || 9 | 7 | 1 ||
                ++---+---+---++---+---+---++---+---+---++
                || 9 | 6 | 2 || 5 | 1 | 7 || 3 | 8 | 4 ||
                ++---+---+---++---+---+---++---+---+---++
                || 4 | 1 | 7 || 3 | 9 | 8 || 2 | 6 | 5 ||
                ++===+===+===++===+===+===++===+===+===++
                || 7 | 2 | 3 || 8 | 6 | 4 || 1 | 5 | 9 ||
                ++---+---+---++---+---+---++---+---+---++
                || 5 | 9 | 8 || 7 | 2 | 1 || 4 | 3 | 6 ||
                ++---+---+---++---+---+---++---+---+---++
                || 6 | 4 | 1 || 9 | 5 | 3 || 8 | 2 | 7 ||
                ++===+===+===++===+===+===++===+===+===++
                || 2 | 7 | 9 || 4 | 8 | 5 || 6 | 1 | 3 ||
                ++---+---+---++---+---+---++---+---+---++
                || 8 | 5 | 6 || 1 | 3 | 9 || 7 | 4 | 2 ||
                ++---+---+---++---+---+---++---+---+---++
                || 1 | 3 | 4 || 2 | 7 | 6 || 5 | 9 | 8 ||
                ++===+===+===++===+===+===++===+===+===++
                """)
        )

    def testSolveHard(self):
        self.maxDiff = None
        HD = Matrix(HARD_SAMPLE_MATRIX, verbose=TEST_CASE_VERBOSE)
        HD.solve()
        self.assertEqual(
            HD.__str__(),
            dedent("""\
                ++===+===+===++===+===+===++===+===+===++
                || 1 | 6 | 9 || 2 | 8 | 3 || 5 | 7 | 4 ||
                ++---+---+---++---+---+---++---+---+---++
                || 2 | 5 | 4 || 9 | 7 | 6 || 1 | 3 | 8 ||
                ++---+---+---++---+---+---++---+---+---++
                || 3 | 7 | 8 || 1 | 5 | 4 || 6 | 2 | 9 ||
                ++===+===+===++===+===+===++===+===+===++
                || 4 | 8 | 5 || 3 | 2 | 9 || 7 | 6 | 1 ||
                ++---+---+---++---+---+---++---+---+---++
                || 9 | 1 | 3 || 5 | 6 | 7 || 8 | 4 | 2 ||
                ++---+---+---++---+---+---++---+---+---++
                || 7 | 2 | 6 || 8 | 4 | 1 || 9 | 5 | 3 ||
                ++===+===+===++===+===+===++===+===+===++
                || 8 | 9 | 2 || 7 | 3 | 5 || 4 | 1 | 6 ||
                ++---+---+---++---+---+---++---+---+---++
                || 6 | 3 | 7 || 4 | 1 | 8 || 2 | 9 | 5 ||
                ++---+---+---++---+---+---++---+---+---++
                || 5 | 4 | 1 || 6 | 9 | 2 || 3 | 8 | 7 ||
                ++===+===+===++===+===+===++===+===+===++
                """)
        )


if __name__ == '__main__':
    unittest.main()
